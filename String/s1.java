
//Write a Java program to get the character at the given index within the String
public class s1 {
    public static void main(String args[]) {

        String a = "Hay fahim";
        System.out.println("The sting: " + a);

        int a1 = a.charAt(0);
        int a2 = a.charAt(7);
        System.out.println("The character a1 is:" + (char) a1);
        System.out.println("The character a2:" + (char) a2);

    }

}
